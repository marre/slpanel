/**
 * SL departure board bitmap font.
 *
 * Core glyphs traced from zmullett/Stockholm-SL-sign-font
 * (https://github.com/zmullett/Stockholm-SL-sign-font), which digitised the
 * dot-matrix characters used on Stockholm T-bana departure signs.
 *
 * Missing characters (digits 2, 9; most uppercase and several lowercase
 * letters; Swedish Ä/Ö; punctuation) were designed to match the proportional,
 * 2-pixel-stroke style of the originals.
 *
 * Each glyph is an array of row strings.
 *   'x' = lit pixel   ' ' = dark pixel
 * All rows within a glyph have the same length (= the glyph width).
 * The normal character body is CELL_HEIGHT (10) rows tall.
 * Glyphs with descenders (g, j, p, q, y, comma, semicolon) have 12 rows;
 * the two extra rows sit below the baseline.
 */

/** Height of the standard character body (rows 0–9). */
export const CELL_HEIGHT = 10;

/** Extra rows below the baseline used by descender characters. */
export const DESCENDER_HEIGHT = 2;

export interface Glyph {
  readonly rows: readonly string[];
  /** Width in pixels (= length of any row string). */
  readonly width: number;
}

export interface BitmapFont {
  readonly cellHeight: number;
  readonly getGlyph: (char: string) => Glyph | undefined;
}

