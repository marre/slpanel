function buildGlyph(rows: readonly string[]): Glyph {
  const width = Math.max(...rows.map((r) => r.length));
  // Pad every row to the same width so callers don't need to range-check.
  const padded = rows.map((r) => r.padEnd(width, ' '));
  return { rows: padded, width };
}

export const GLYPHS: Readonly<Record<string, Glyph>> = Object.fromEntries(
  Object.entries(RAW).map(([ch, rows]) => [ch, buildGlyph(rows)]),
);

/** Look up a glyph by character. Returns undefined for unknown characters. */
export function getGlyph(char: string): Glyph | undefined {
  return GLYPHS[char];
}

